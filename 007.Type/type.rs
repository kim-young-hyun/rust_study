// 변수 타입별 범위

fn main() {
    println!("{} - max: {}, min: {}", "i8", i8::MAX, i8::MIN);
    println!("{} - max: {}, min: {}", "i16", i16::MAX, i16::MIN);
    println!("{} - max: {}, min: {}", "i32", i32::MAX, i32::MIN);
    println!("{} - max: {}, min: {}", "i64", i64::MAX, i64::MIN);
    println!("{} - max: {}, min: {}", "i128", i128::MAX, i128::MIN);
}